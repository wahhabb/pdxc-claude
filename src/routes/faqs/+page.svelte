			<article class="page-content">
				<div class="faq-item">
					<h2>What is Cohousing?</h2>
					<p>Cohousing, imported from Denmark, refers to an intentional community of private homes and shared spaces where neighbors know, care for and support each other. Cohousing communities can take many forms, but in essence, they collaborate on managing community business and activities and usually share some regularly scheduled meals. For more details, see <a href="https://www.cohousing.org/what-cohousing/cohousing/" target="_blank" rel="noopener noreferrer">cohousing.org's description</a>.</p>
				</div>

				<div class="faq-item">
					<h2>Do I Own My Own Home?</h2>
					<p>Yes. Our members have a private, modern condo unit with a full kitchen and one or more bathrooms.</p>
				</div>

				<div class="faq-item">
					<h2>Do I Also Own a Share of the Community?</h2>
					<p>Yes. Along with other households, you own an undivided share of the common spaces, which include a dining/meeting room with a commercial-style kitchen, living room/library, media room with large-format TV screen, craft and exercise room, laundry room, tool room, bike storage room, office, garage and storage cages, courtyard, roof deck, patio, gardens, hallways, walkways, elevator and utility areas. Most members also own their own individual parking spaces.</p>
				</div>

				<div class="faq-item">
					<h2>How Much Does It Cost?</h2>
					<p>Typically, cohousing projects cost about the same as comparable condominium projects. The individual homes are often smaller to encourage the use of common spaces. Based on the size and specific finishes chosen, our homes initially sold for between $355,000 and $720,000. Future resale prices will vary.</p>
				</div>

				<div class="faq-item">
					<h2>What About HOA Fees?</h2>
					<p>Our current monthly dues range from about $600 to $1100, depending on the size of the home. This includes water, garbage, recycling and community wifi as well as a monthly contribution to our reserve fund. Members pay their own electricity and gas (if they have it), as well as any individual television and Internet connections.</p>
				</div>

				<div class="faq-item">
					<h2>What About Parking?</h2>
					<p>While many of us are planning on reducing or eliminating use of a private vehicle, all but three of our units own a single off-street, secure parking space. Some people have already left their cars behind. We have intentionally located our project to be well served by public transit and private car-sharing services.</p>
				</div>

				<div class="faq-item">
					<h2>How Big Are the Homes?</h2>
					<p>Our homes come in various sizes and configurations to meet the needs of our members. There are one- and two-bedroom homes and some with a third room that can be used as a bedroom or study. Home sizes range from about 625 to 1250 square feet. We also have two guest rooms available for family and friends.</p>
				</div>

				<div class="faq-item">
					<h2>Is This a Care Facility?</h2>
					<p>No. We do not offer skilled nursing care, medical support or memory care. We are not a continuing care retirement community (CCRC), nor a retirement home or convalescent facility. We are a group of active adults who have chosen to live in a tight-knit community of caring, helpful friends. We believe this will enable many of us to stay in our homes longer than if we lived in isolation.</p>
				</div>

				<div class="faq-item">
					<h2>Do You Allow Children?</h2>
					<p>Absolutely! We love children, and they are frequent visitors. We cater to adults 55 and older, but that doesn't mean you can't have a child or younger person living in your unit with you. Under the law, 80 percent of our units must be occupied by at least one person who is 55 or older. The other 20 percent have no age restriction.</p>
				</div>

				<div class="faq-item">
					<h2>Is Cohousing Just a Portland Thing?</h2>
					<p>Cohousing is sweeping the nation. As of 2017, the year we opened, there were about 150 cohousing developments in operation and another 100 in various stages of development. There are 10 or 12 in the Portland area alone. So far, we are the only local cohousing specifically for older adults.</p>
				</div>

				<div class="faq-item">
					<h2>Would I Be Able to Take Extended Trips?</h2>
					<p>Yes. Others in the community will gladly help keep an eye on your home while you are traveling. We have even talked about setting up an international exchange with other cohousing communities around the world.</p>
				</div>

				<div class="faq-item">
					<h2>Where Can Guests Stay?</h2>
					<p>We have two guest bedrooms that can be reserved for visiting friends and family. This allowed us to scale back the need for extra bedrooms.</p>
				</div>

				<div class="faq-item">
					<h2>Do I Have to Participate in Community Meals?</h2>
					<p>No, but we encourage you to do so. It's one of the best ways to socialize and better get to know community members. Before the pandemic, we had two planned community dinners each week, as well as others on special occasions. We are getting back to that model now that we are a fully vaccinated community. Members are expected to help with at least one meal each month, either preparation or cleanup.</p>
				</div>

				<div class="faq-item">
					<h2>How Would I Know If I'd Like PDX Commons?</h2>
					<p>If you are interested in buying a unit that becomes available, you would have opportunities to meet current members and tour the building. The <a href="/our-community/living">Living in Cohousing</a> page describes the role of respect and responsibility when living in community and talks about the kind of people who thrive in such environments.</p>
				</div>

				<div class="faq-item">
					<h2>How Is PDX Commons Different from a Traditional Retirement Community?</h2>
					<p>PDX Commons was designed by our members and is owned and operated by us. There is no corporation or non-profit organization making decisions for us. We are driven by what benefits our members, not a corporate bottom line. Most importantly, PDX Commons is an intentional community. For us, a strong community is an explicit goal, not the lucky coincidence of having good neighbors. We think cohousing is a more pleasant and satisfying way to live, and studies show it can actually be less expensive than comparable private alternatives.</p>
				</div>

				<div class="faq-item">
					<h2>Can I Visit PDX Commons?</h2>
					<p>Yes, we give tours for interested parties upon request (with reasonable notice). We don't, at this time, offer our guest rooms to visitors (other than family and friends), except for those already on our wait list. Contact us through this website to let us know you'd like to visit.</p>
				</div>

				<div class="faq-item">
					<h2>How Can I Join Your Wait Pool?</h2>
					<p>It's simple. Ask us for a tour, or let us know you're interested by using the <a href="/contact">Contact</a> form. We'll invite you to fill out a personal profile form and pay a one-time $50 fee. People in the wait pool are notified before the general public when a home becomes available for sale. We call it a "pool" because there is no priority involved. We recommend that you consider whether cohousing is right for you before you sign up to be in our wait pool. See our <a href="/our-community/living">Living in Cohousing</a> page.</p>
				</div>

				<div class="faq-item">
					<h2>Do You Have a Mailing List?</h2>
					<p>Yes, we have a mailing list for our e-newsletter. We already have over 800 people on the list. Just let us know via our <a href="/contact">contact page</a> if you'd like to be added.</p>
				</div>
